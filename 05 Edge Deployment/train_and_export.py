def load_bmw_trips():
    """Load and preprocess all BMW i3 trip CSVs."""
    trip_files = sorted(BMW_DIR.glob("Trip*.csv"))
    all_data = []

    for f in trip_files:
        df = pd.read_csv(f, sep=';', encoding='latin1')
        trip_name = f.stem
        is_winter = 1 if trip_name.startswith("TripB") else 0

        # Find columns
        col_map = {
            'time': find_col(df, 'Time'),
            'velocity': find_col(df, 'Velocity'),
            'elevation': find_col(df, 'Elevation'),
            'throttle': find_col(df, 'Throttle'),
            'torque': find_col(df, 'Motor Torque') or find_col(df, 'Torque'),
            'accel': find_col(df, 'Longitudinal Accel') or find_col(df, 'Accel'),
            'regen': find_col(df, 'Regenerative'),
            'voltage': find_col(df, 'Battery Voltage') or find_col(df, 'Voltage'),
            'current': find_col(df, 'Battery Current') or find_col(df, 'Current'),
            'btemp': find_col(df, 'Battery Temp'),
            'soc': find_col(df, 'SoC'),
            'heating': find_col(df, 'Heating Power CAN') or find_col(df, 'Heating'),
            'aircon': find_col(df, 'AirCon'),
            'atemp': find_col(df, 'Ambient Temp'),
        }

        if col_map['soc'] is None or col_map['voltage'] is None or col_map['velocity'] is None:
            continue

        # Match notebook: use iloc[0] - iloc[-1] (NaN trips get filtered out)
        soc_first = df[col_map['soc']].iloc[0]
        soc_last = df[col_map['soc']].iloc[-1]
        soc_change = soc_first - soc_last
        if not (soc_change > 2):  # NaN fails this check, matching notebook behavior
            continue

        all_data.append((df, col_map, trip_name, is_winter))

    print(f"  Loaded {len(all_data)} valid BMW i3 trips")
    return all_data


def extract_soc_features(trips):
    """Extract windowed features for SoC prediction."""
    WINDOW = 600
    STRIDE = 200
    BATTERY_KWH = 33.0

    rows = []
    for df, cm, trip_name, is_winter in trips:
        time_col = df[cm['time']].values.astype(float)
        voltage = df[cm['voltage']].values.astype(float)
        current = df[cm['current']].values.astype(float)
        soc = df[cm['soc']].values.astype(float)

        btemp = df[cm['btemp']].values.astype(float) if cm['btemp'] else np.zeros(len(df))
        atemp = df[cm['atemp']].values.astype(float) if cm['atemp'] else np.zeros(len(df))
        vel = df[cm['velocity']].values.astype(float) if cm['velocity'] else np.zeros(len(df))
        accel = df[cm['accel']].values.astype(float) if cm['accel'] else np.zeros(len(df))
        torque = df[cm['torque']].values.astype(float) if cm['torque'] else np.zeros(len(df))
        elev = df[cm['elevation']].values.astype(float) if cm['elevation'] else np.zeros(len(df))

        heating = df[cm['heating']].values.astype(float) if cm['heating'] else np.zeros(len(df))
        aircon = df[cm['aircon']].values.astype(float) if cm['aircon'] else np.zeros(len(df))
        hvac = np.abs(heating) * 1000 + np.abs(aircon) * 1000

        power = voltage * current

        # Cumulative features
        dt = np.diff(time_col, prepend=time_col[0])
        dt[0] = 0
        cum_ah = np.cumsum(current * dt / 3600)
        cum_wh = np.cumsum(power * dt / 3600)
        cum_dist = np.cumsum(np.abs(vel) / 3600 * dt)

        n = len(df)
        for start in range(0, n - WINDOW, STRIDE):
            end = start + WINDOW
            mid = (start + end) // 2

            w_v = voltage[start:end]
            w_i = current[start:end]
            w_p = power[start:end]
            w_vel = vel[start:end]
            w_acc = accel[start:end]
            w_torq = torque[start:end]
            w_bt = btemp[start:end]
            w_at = atemp[start:end]
            w_hvac = hvac[start:end]
            w_elev = elev[start:end]

            rows.append({
                'Avg_Voltage': np.mean(w_v),
                'Avg_Current': np.mean(w_i),
                'Avg_Power': np.mean(w_p),
                'Avg_Velocity': np.mean(w_vel),
                'Avg_Accel': np.mean(w_acc),
                'Avg_Torque': np.mean(w_torq),
                'Avg_Battery_Temp': np.mean(w_bt),
                'Avg_Ambient_Temp': np.mean(w_at),
                'Avg_HVAC': np.mean(w_hvac),
                'Max_Current': np.max(w_i),
                'Min_Current': np.min(w_i),
                'Std_Current': np.std(w_i),
                'Std_Velocity': np.std(w_vel),
                'Max_Power': np.max(w_p),
                'Min_Voltage': np.min(w_v),
                'Cumulative_Ah': cum_ah[mid],
                'Cumulative_Wh': cum_wh[mid],
                'Cumulative_Dist_km': cum_dist[mid],
                'Elapsed_s': time_col[mid] - time_col[0],
                'Elevation_Change': w_elev[-1] - w_elev[0] if len(w_elev) > 0 else 0,
                'Temp_Diff': np.mean(w_bt) - np.mean(w_at),
                'Is_Winter': is_winter,
                'SOC': soc[mid],
                'trip': trip_name,
            })

    return pd.DataFrame(rows)


def train_soc_model():
    """Train SoC model and export as C header."""
    print("\n" + "="*60)
    print("MODEL 1: SoC Prediction (Lasso + Polynomial)")
    print("="*60)

    trips = load_bmw_trips()
    df = extract_soc_features(trips)
    df = df.dropna()
    print(f"  Total windows: {len(df)}")

    feature_cols = [c for c in df.columns if c not in ('SOC', 'trip')]
    X = df[feature_cols].values
    y = df['SOC'].values

    # Trip-level split (matching notebook: train_test_split on trip name list in load order)
    # The notebook passes the trip names in glob-sorted order to train_test_split
    unique_trips = list(dict.fromkeys(df['trip']))  # preserve insertion order (= glob order)
    train_trips_list, test_trips_list = train_test_split(
        unique_trips, test_size=0.25, random_state=42
    )
    train_trips = set(train_trips_list)
    print(f"  Train: {len(train_trips_list)} trips, Test: {len(test_trips_list)} trips")

    train_mask = df['trip'].isin(train_trips)
    X_train, X_test = X[train_mask], X[~train_mask]
    y_train, y_test = y[train_mask], y[~train_mask]

    # Scale
    scaler = StandardScaler()
    X_train_s = scaler.fit_transform(X_train)
    X_test_s = scaler.transform(X_test)

    # XGBoost (ESP32-constrained: shallow trees, limited count)
    # The notebook's XGBoost tuned achieved R²≈0.88 on all 22 features
    model = xgb.XGBRegressor(
        n_estimators=100,  # keep small for ESP32
        max_depth=4,
        learning_rate=0.08,
        subsample=0.8,
        colsample_bytree=0.8,
        min_child_weight=8,
        reg_alpha=0.5,
        reg_lambda=2.0,
        tree_method='hist',
        random_state=42,
    )
    model.fit(X_train_s, y_train)

    y_pred = model.predict(X_test_s)
    r2 = r2_score(y_test, y_pred)
    mae = mean_absolute_error(y_test, y_pred)
    print(f"  XGBoost (ESP32-sized) -> R²={r2:.4f}, MAE={mae:.2f}%")

    # Save feature metadata
    meta = {
        'feature_names': feature_cols,
        'scaler_mean': scaler.mean_.tolist(),
        'scaler_scale': scaler.scale_.tolist(),
        'r2': r2, 'mae': mae,
    }
    (OUT_DIR / "soc_meta.json").write_text(json.dumps(meta, indent=2))

    # Export as C header
    if not export_xgb_emlearn_header("soc", model, "model_soc.h"):
        export_xgb_manual_header(
            "soc", model,
            scaler.mean_, scaler.scale_,
            len(feature_cols), "model_soc.h"
        )

    return r2, mae


