def _extract_base_features_for_battery(meta_df, battery_id):
    """Extract per-cycle features for one battery (matches notebook exactly)."""
    dis = meta_df[(meta_df.battery_id == battery_id) &
                  (meta_df.type == 'discharge') &
                  (meta_df.Capacity.notna())]
    imp = meta_df[(meta_df.battery_id == battery_id) &
                  (meta_df.type == 'impedance')]

    features = []
    for idx, (_, row) in enumerate(dis.iterrows()):
        csv_path = NASA_DIR / "data" / row['filename']
        try:
            ddf = pd.read_csv(csv_path)
        except:
            continue

        v = ddf['Voltage_measured'].values
        i = ddf['Current_measured'].values
        t = ddf['Temperature_measured'].values
        time_s = ddf['Time'].values

        # Impedance: use last impedance measurement BEFORE this discharge (by uid)
        prior_imp = imp[imp.uid < row['uid']]
        if len(prior_imp) > 0:
            last_imp = prior_imp.iloc[-1]
            re_val = last_imp['Re'] if pd.notna(last_imp['Re']) else np.nan
            rct_val = last_imp['Rct'] if pd.notna(last_imp['Rct']) else np.nan
        else:
            re_val = np.nan
            rct_val = np.nan

        features.append({
            'battery_id': battery_id,
            'cycle': idx,  # 0-based, matching notebook
            'capacity': row['Capacity'],
            'v_mean': np.mean(v), 'v_std': np.std(v),
            'v_min': np.min(v), 'v_max': np.max(v),
            'v_range': np.max(v) - np.min(v),
            'i_mean': np.mean(i), 'i_std': np.std(i),
            'i_min': np.min(i), 'i_max': np.max(i),
            't_mean': np.mean(t), 't_std': np.std(t),
            't_max': np.max(t), 't_rise': np.max(t) - t[0],
            'discharge_time': time_s[-1] if len(time_s) > 0 else 0,
            'energy': np.trapz(v * np.abs(i), time_s) / 3600 if len(time_s) > 1 else 0,
            'Re': re_val, 'Rct': rct_val,
        })
    return pd.DataFrame(features)


def extract_rul_features(meta):
    """Extract per-cycle features for RUL prediction (matches notebook pipeline)."""
    RATED_CAPACITY = 2.0
    EOL_THRESHOLD = 1.4

    # Step 1: Extract base features per battery
    all_frames = []
    for batt in sorted(meta['battery_id'].unique()):
        bdf = _extract_base_features_for_battery(meta, batt)
        if len(bdf) > 0:
            all_frames.append(bdf)
    df = pd.concat(all_frames, ignore_index=True)

    # Step 2: Add RUL and SoH
    result = []
    for bid in df.battery_id.unique():
        bdf = df[df.battery_id == bid].copy()
        eol_rows = bdf[bdf.capacity <= EOL_THRESHOLD]
        if len(eol_rows) > 0:
            eol_cycle = eol_rows.iloc[0]['cycle']
        else:
            eol_cycle = bdf['cycle'].max()
        bdf['RUL'] = eol_cycle - bdf['cycle']
        bdf['SoH'] = bdf['capacity'] / RATED_CAPACITY * 100
        result.append(bdf)
    df = pd.concat(result, ignore_index=True)

    # Step 3: Remove post-EOL samples
    pre_clip = len(df)
    df = df[df.RUL >= 0].copy()
    print(f"  Removed {pre_clip - len(df)} post-EOL samples")

    # Step 4: Handle missing impedance (ffill then bfill, matching notebook)
    df['Re'] = df.groupby('battery_id')['Re'].ffill()
    df['Rct'] = df.groupby('battery_id')['Rct'].bfill()
    df['Re'] = df.groupby('battery_id')['Re'].bfill()
    df['Rct'] = df.groupby('battery_id')['Rct'].bfill()
    df = df.dropna(subset=['Re', 'Rct'])

    # Step 5: Degradation features (matching notebook exactly)
    for w in [3, 5]:
        df[f'cap_roll{w}'] = df.groupby('battery_id')['capacity'].transform(
            lambda x: x.rolling(w, min_periods=1).mean())
        df[f'cap_std{w}'] = df.groupby('battery_id')['capacity'].transform(
            lambda x: x.rolling(w, min_periods=1).std().fillna(0))

    df['SoH_delta'] = df.groupby('battery_id')['SoH'].diff().fillna(0)
    df['cap_delta'] = df.groupby('battery_id')['capacity'].diff().fillna(0)
    df['Re_delta'] = df.groupby('battery_id')['Re'].diff().fillna(0)
    df['energy_delta'] = df.groupby('battery_id')['energy'].diff().fillna(0)

    df['SoH_rate'] = df.groupby('battery_id', group_keys=False).apply(
        lambda g: (g['SoH'].iloc[0] - g['SoH']) / (g['cycle'] - g['cycle'].iloc[0] + 1)
    ).reset_index(level=0, drop=True) if False else np.nan  # placeholder
    # Manual SoH_rate to avoid groupby.apply issues
    soh_rate = []
    for bid in df.battery_id.unique():
        mask = df.battery_id == bid
        g = df[mask]
        first_soh = g['SoH'].iloc[0]
        first_cycle = g['cycle'].iloc[0]
        soh_rate.append((first_soh - g['SoH']) / (g['cycle'] - first_cycle + 1))
    df['SoH_rate'] = pd.concat(soh_rate)

    df['cap_fade'] = df.groupby('battery_id')['capacity'].transform(
        lambda x: (x.iloc[0] - x) / x.iloc[0])

    # Lag features
    for lag in [1, 2, 3]:
        df[f'SoH_lag{lag}'] = df.groupby('battery_id')['SoH'].shift(lag)
        df[f'cap_lag{lag}'] = df.groupby('battery_id')['capacity'].shift(lag)
        if lag <= 2:
            df[f'Re_lag{lag}'] = df.groupby('battery_id')['Re'].shift(lag)

    for lag in [1, 2, 3]:
        df[f'SoH_lag{lag}'] = df[f'SoH_lag{lag}'].fillna(df['SoH'])
        df[f'cap_lag{lag}'] = df[f'cap_lag{lag}'].fillna(df['capacity'])
        if lag <= 2:
            df[f'Re_lag{lag}'] = df[f'Re_lag{lag}'].fillna(df['Re'])

    # Interaction features
    df['SoH_x_rate'] = df['SoH'] * df['SoH_rate']
    df['SoH_x_delta'] = df['SoH'] * df['SoH_delta']
    df['cap_x_Re'] = df['capacity'] * df['Re']
    df['SoH_sq'] = df['SoH'] ** 2
    df['cap_fade_sq'] = df['cap_fade'] ** 2

    df = df.dropna()
    print(f"  Total cycles (RUL): {len(df)}, batteries: {df['battery_id'].nunique()}")
    return df


def train_rul_model():
    """Train RUL model (RidgeCV — linear) and export."""
    print("\n" + "="*60)
    print("MODEL 4: RUL Prediction (RidgeCV)")
    print("="*60)

    meta = load_nasa_data()
    df = extract_rul_features(meta)

    # Exact feature list matching notebook
    feature_cols = ['cycle', 'SoH', 'v_mean', 'v_std', 'v_min', 'v_max', 'v_range',
                    'i_mean', 'i_std', 'i_min', 'i_max',
                    't_mean', 't_std', 't_max', 't_rise',
                    'discharge_time', 'energy', 'Re', 'Rct',
                    'SoH_delta', 'cap_delta', 'Re_delta', 'energy_delta',
                    'SoH_rate', 'cap_fade',
                    'cap_roll3', 'cap_roll5', 'cap_std3', 'cap_std5',
                    'SoH_lag1', 'SoH_lag2', 'SoH_lag3',
                    'cap_lag1', 'cap_lag2', 'cap_lag3',
                    'Re_lag1', 'Re_lag2',
                    'SoH_x_rate', 'SoH_x_delta', 'cap_x_Re',
                    'SoH_sq', 'cap_fade_sq']

    X = df[feature_cols].values
    y = df['RUL'].values

    # Hybrid temporal split (matching notebook exactly)
    classic = ['B0005', 'B0006', 'B0007', 'B0018']
    train_rows, test_rows = [], []
    for bid in sorted(df.battery_id.unique()):
        bdf = df[df.battery_id == bid].sort_values('cycle')
        if bid in classic:
            n = len(bdf)
            split = int(n * 0.7)
            train_rows.append(bdf.iloc[:split])
            test_rows.append(bdf.iloc[split:])
        else:
            train_rows.append(bdf)

    train_df = pd.concat(train_rows, ignore_index=True)
    test_df = pd.concat(test_rows, ignore_index=True)
    print(f"  Train: {len(train_df)} samples, Test: {len(test_df)} samples")

    X_train = train_df[feature_cols].values
    y_train = train_df['RUL'].values
    X_test = test_df[feature_cols].values
    y_test = test_df['RUL'].values

    scaler = StandardScaler()
    X_train_s = scaler.fit_transform(X_train)
    X_test_s = scaler.transform(X_test)

    model = RidgeCV(alphas=np.logspace(-2, 4, 50))
    model.fit(X_train_s, y_train)

    y_pred = model.predict(X_test_s)
    r2 = r2_score(y_test, y_pred)
    mae = mean_absolute_error(y_test, y_pred)
    print(f"  RidgeCV -> R²={r2:.4f}, MAE={mae:.1f} cycles, alpha={model.alpha_:.4f}")

    meta_out = {
        'feature_names': feature_cols,
        'scaler_mean': scaler.mean_.tolist(),
        'scaler_scale': scaler.scale_.tolist(),
        'r2': r2, 'mae': mae,
        'n_features': len(feature_cols),
    }
    (OUT_DIR / "rul_meta.json").write_text(json.dumps(meta_out, indent=2))

    export_linear_model_header(
        name="rul",
        weights=model.coef_,
        bias=model.intercept_,
        scaler_mean=scaler.mean_,
        scaler_scale=scaler.scale_,
        feature_names=feature_cols,
        filename="model_rul.h",
        poly_powers=None,
    )

    return r2, mae


