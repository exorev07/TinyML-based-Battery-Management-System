def load_nasa_data():
    """Load NASA battery dataset with metadata."""
    meta = pd.read_csv(NASA_DIR / "metadata.csv")
    # Coerce non-numeric impedance values to NaN (matching notebook behavior)
    meta['Re'] = pd.to_numeric(meta['Re'], errors='coerce')
    meta['Rct'] = pd.to_numeric(meta['Rct'], errors='coerce')
    meta['Capacity'] = pd.to_numeric(meta['Capacity'], errors='coerce')
    # Sanitize: impedance values should be small positive numbers (< 10 Ohm)
    # Corrupt values in B0050/B0052 (e.g. -9.7e14) → NaN
    meta.loc[meta['Re'].abs() > 10, 'Re'] = np.nan
    meta.loc[meta['Rct'].abs() > 10, 'Rct'] = np.nan
    return meta


def extract_soh_features(meta):
    """Extract per-cycle features for SoH prediction."""
    RATED_CAPACITY = 2.0

    rows = []
    batteries = meta['battery_id'].unique()

    for batt in batteries:
        batt_meta = meta[meta['battery_id'] == batt].sort_values('uid')
        cycle = 0

        # Get impedance values (forward fill)
        impedance_re = []
        impedance_rct = []
        for _, row in batt_meta.iterrows():
            if row['type'] == 'impedance':
                impedance_re.append(row.get('Re', np.nan))
                impedance_rct.append(row.get('Rct', np.nan))
            elif row['type'] == 'discharge':
                cycle += 1
                # Load discharge data
                csv_path = NASA_DIR / "data" / row['filename']
                if not csv_path.exists():
                    continue

                try:
                    ddf = pd.read_csv(csv_path)
                except:
                    continue

                if len(ddf) < 5:
                    continue

                v = ddf['Voltage_measured'].values
                i = ddf['Current_measured'].values
                t = ddf['Temperature_measured'].values
                time_s = ddf['Time'].values

                capacity = row.get('Capacity', np.nan)
                if pd.isna(capacity):
                    continue

                soh = (capacity / RATED_CAPACITY) * 100

                # Get latest impedance
                re_val = impedance_re[-1] if impedance_re else np.nan
                rct_val = impedance_rct[-1] if impedance_rct else np.nan

                # Compute energy
                energy = np.trapz(v * np.abs(i), time_s) / 3600 if len(time_s) > 1 else 0

                rows.append({
                    'battery_id': batt,
                    'cycle': cycle,
                    'v_mean': np.mean(v), 'v_std': np.std(v),
                    'v_min': np.min(v), 'v_max': np.max(v),
                    'v_range': np.max(v) - np.min(v),
                    'i_mean': np.mean(i), 'i_std': np.std(i),
                    'i_min': np.min(i), 'i_max': np.max(i),
                    't_mean': np.mean(t), 't_std': np.std(t),
                    't_max': np.max(t), 't_rise': np.max(t) - t[0],
                    'discharge_time': time_s[-1] if len(time_s) > 0 else 0,
                    'energy': energy,
                    'Re': re_val, 'Rct': rct_val,
                    'SoH': soh,
                    'capacity': capacity,
                })

    df = pd.DataFrame(rows)
    # Forward/back fill impedance within each battery
    df['Re'] = df.groupby('battery_id')['Re'].ffill().bfill()
    df['Rct'] = df.groupby('battery_id')['Rct'].ffill().bfill()
    df = df.dropna()

    print(f"  Total cycles: {len(df)}, batteries: {df['battery_id'].nunique()}")
    return df


def train_soh_model():
    """Train SoH model (LassoCV + Poly) and export."""
    print("\n" + "="*60)
    print("MODEL 3: SoH Prediction (LassoCV + Polynomial)")
    print("="*60)

    meta = load_nasa_data()
    df = extract_soh_features(meta)

    feature_cols = ['cycle', 'v_mean', 'v_std', 'v_min', 'v_max', 'v_range',
                    'i_mean', 'i_std', 'i_min', 'i_max',
                    't_mean', 't_std', 't_max', 't_rise',
                    'discharge_time', 'energy', 'Re', 'Rct']

    X = df[feature_cols].values
    y = df['SoH'].values
    groups = df['battery_id'].values

    # Cross-battery split: classic batteries for test
    classic = {'B0005', 'B0006', 'B0007', 'B0018'}
    test_mask = df['battery_id'].isin(classic)
    X_train, X_test = X[~test_mask], X[test_mask]
    y_train, y_test = y[~test_mask], y[test_mask]
    groups_train = df[~test_mask]['battery_id'].values

    scaler = StandardScaler()
    X_train_s = scaler.fit_transform(X_train)
    X_test_s = scaler.transform(X_test)

    # Top 10 by correlation
    correlations = np.array([abs(np.corrcoef(X_train_s[:, i], y_train)[0, 1])
                             for i in range(X_train_s.shape[1])])
    top_idx = np.argsort(correlations)[-10:][::-1]
    top_names = [feature_cols[i] for i in top_idx]
    print(f"  Top 10 features: {top_names}")

    poly = PolynomialFeatures(degree=2, include_bias=False)
    X_train_poly = poly.fit_transform(X_train_s[:, top_idx])
    X_test_poly = poly.transform(X_test_s[:, top_idx])

    gkf = GroupKFold(n_splits=5)
    model = LassoCV(alphas=np.logspace(-4, 2, 50), max_iter=10000,
                     cv=gkf.split(X_train_poly, y_train, groups_train), random_state=42)
    model.fit(X_train_poly, y_train)

    y_pred = model.predict(X_test_poly)
    r2 = r2_score(y_test, y_pred)
    mae = mean_absolute_error(y_test, y_pred)
    print(f"  LassoCV Poly -> R²={r2:.4f}, MAE={mae:.2f}%, alpha={model.alpha_:.4f}")

    meta_out = {
        'feature_names': feature_cols,
        'top_feature_indices': top_idx.tolist(),
        'top_feature_names': top_names,
        'scaler_mean': scaler.mean_.tolist(),
        'scaler_scale': scaler.scale_.tolist(),
        'r2': r2, 'mae': mae,
    }
    (OUT_DIR / "soh_meta.json").write_text(json.dumps(meta_out, indent=2))

    poly_powers = poly.powers_.astype(int)
    export_linear_model_header(
        name="soh",
        weights=model.coef_,
        bias=model.intercept_,
        scaler_mean=scaler.mean_[top_idx],
        scaler_scale=scaler.scale_[top_idx],
        feature_names=top_names,
        filename="model_soh.h",
        poly_powers=poly_powers,
    )

    return r2, mae


