def _parse_impedance(val):
    """Parse impedance value, handling complex number strings.
    Re and Rct are real-valued quantities; complex strings use .real part."""
    try:
        return float(val)
    except (ValueError, TypeError):
        try:
            c = complex(val)
            return c.real
        except (ValueError, TypeError):
            return np.nan


def find_col(df, pattern):
    """Find a column by substring match (handles encoding issues in BMW CSV)."""
    for c in df.columns:
        if pattern.lower() in c.lower():
            return c
    return None


def export_linear_model_header(name, weights, bias, scaler_mean, scaler_scale,
                                feature_names, filename, poly_powers=None):
    """Export a linear model (Ridge/Lasso) as a C header file.

    If poly_powers is provided, the model uses polynomial features.
    poly_powers is a 2D array of shape (n_poly_features, n_base_features)
    representing the exponent of each base feature for each poly term.
    """
    n_features = len(weights)
    n_base = len(scaler_mean)

    lines = []
    lines.append(f"// Auto-generated by train_and_export.py")
    lines.append(f"// Model: {name}")
    lines.append(f"#ifndef MODEL_{name.upper()}_H")
    lines.append(f"#define MODEL_{name.upper()}_H")
    lines.append(f"")
    lines.append(f"#define {name.upper()}_N_BASE_FEATURES {n_base}")
    lines.append(f"#define {name.upper()}_N_FEATURES {n_features}")
    lines.append(f"")

    # Scaler parameters (for base features)
    lines.append(f"static const float {name}_scaler_mean[{n_base}] = {{")
    lines.append(f"  {', '.join(f'{v:.8f}f' for v in scaler_mean)}")
    lines.append(f"}};")
    lines.append(f"static const float {name}_scaler_scale[{n_base}] = {{")
    lines.append(f"  {', '.join(f'{v:.8f}f' for v in scaler_scale)}")
    lines.append(f"}};")
    lines.append(f"")

    # Model weights and bias
    lines.append(f"static const float {name}_weights[{n_features}] = {{")
    # Split into lines of 8 for readability
    for i in range(0, n_features, 8):
        chunk = weights[i:i+8]
        lines.append(f"  {', '.join(f'{v:.8f}f' for v in chunk)},")
    lines.append(f"}};")
    lines.append(f"static const float {name}_bias = {bias:.8f}f;")
    lines.append(f"")

    # Polynomial powers if applicable
    if poly_powers is not None:
        n_poly = poly_powers.shape[0]
        n_base_poly = poly_powers.shape[1]
        lines.append(f"#define {name.upper()}_USE_POLY 1")
        lines.append(f"#define {name.upper()}_N_POLY_BASE {n_base_poly}")
        lines.append(f"// Polynomial exponent matrix: [{n_poly}][{n_base_poly}]")
        lines.append(f"static const uint8_t {name}_poly_powers[{n_poly}][{n_base_poly}] = {{")
        for row in poly_powers:
            lines.append(f"  {{{', '.join(str(int(v)) for v in row)}}},")
        lines.append(f"}};")
    else:
        lines.append(f"#define {name.upper()}_USE_POLY 0")

    lines.append(f"")

    # Inline predict function
    lines.append(f"static inline float {name}_predict(const float raw_features[{n_base}]) {{")
    lines.append(f"  // 1. Scale base features")
    lines.append(f"  float scaled[{n_base}];")
    lines.append(f"  for (int i = 0; i < {n_base}; i++) {{")
    lines.append(f"    scaled[i] = (raw_features[i] - {name}_scaler_mean[i]) / {name}_scaler_scale[i];")
    lines.append(f"  }}")
    lines.append(f"")

    if poly_powers is not None:
        n_poly = poly_powers.shape[0]
        n_base_poly = poly_powers.shape[1]
        lines.append(f"  // 2. Compute polynomial features")
        lines.append(f"  float poly[{n_poly}];")
        lines.append(f"  for (int i = 0; i < {n_poly}; i++) {{")
        lines.append(f"    poly[i] = 1.0f;")
        lines.append(f"    for (int j = 0; j < {n_base_poly}; j++) {{")
        lines.append(f"      for (int p = 0; p < {name}_poly_powers[i][j]; p++) {{")
        lines.append(f"        poly[i] *= scaled[j];")  # poly base indices = first n_base_poly of scaled
        lines.append(f"      }}")
        lines.append(f"    }}")
        lines.append(f"  }}")
        lines.append(f"")
        lines.append(f"  // 3. Dot product")
        lines.append(f"  float result = {name}_bias;")
        lines.append(f"  for (int i = 0; i < {n_features}; i++) {{")
        lines.append(f"    result += {name}_weights[i] * poly[i];")
        lines.append(f"  }}")
    else:
        lines.append(f"  // 2. Dot product")
        lines.append(f"  float result = {name}_bias;")
        lines.append(f"  for (int i = 0; i < {n_features}; i++) {{")
        lines.append(f"    result += {name}_weights[i] * scaled[i];")
        lines.append(f"  }}")

    lines.append(f"  return result;")
    lines.append(f"}}")
    lines.append(f"")
    lines.append(f"#endif // MODEL_{name.upper()}_H")

    filepath = HEADER_DIR / filename
    filepath.write_text("\n".join(lines))
    print(f"  -> Exported {filepath}")


def export_xgb_emlearn_header(name, model, filename):
    """Export XGBoost model using emlearn (tree ensemble -> C code)."""
    try:
        import emlearn
        cmodel = emlearn.convert(model, method='inline')
        filepath = HEADER_DIR / filename
        cmodel.save(file=str(filepath), name=name)
        print(f"  -> Exported {filepath} (via emlearn)")
        return True
    except ImportError:
        print("  !! emlearn not installed, falling back to manual export")
        return False


def export_xgb_manual_header(name, model, scaler_mean, scaler_scale, n_base, filename):
    """Fallback: export XGBoost as a JSON ensemble and write a C header with
    a simple tree-walk interpreter. Works without emlearn."""

    # Get trees as text
    booster = model.get_booster()
    trees_json = booster.get_dump(dump_format='json')

    import json as json_mod

    # Parse all trees into flat node arrays
    all_nodes = []  # list of trees, each tree = list of (feature_idx, threshold, left, right, leaf_value)

    def flatten_tree(node, nodes_list):
        idx = len(nodes_list)
        if 'leaf' in node:
            nodes_list.append((-1, node['leaf'], -1, -1))
            return idx
        else:
            # placeholder
            nodes_list.append(None)
            feat = int(node['split'][1:]) if node['split'].startswith('f') else int(node['split'])
            threshold = float(node['split_condition'])
            left_idx = flatten_tree(node['children'][0], nodes_list)
            right_idx = flatten_tree(node['children'][1], nodes_list)
            nodes_list[idx] = (feat, threshold, left_idx, right_idx)
            return idx

    for tree_str in trees_json:
        tree_dict = json_mod.loads(tree_str)
        nodes = []
        flatten_tree(tree_dict, nodes)
        all_nodes.append(nodes)

    n_trees = len(all_nodes)
    # XGBoost 2.0+ auto-learns base_score (returns None from get_params).
    # Read the actual learned value from the booster config.
    try:
        import json as _json
        _cfg = _json.loads(model.get_booster().save_config())
        _bs_str = _cfg['learner']['learner_model_param']['base_score']
        # May be a string like "[7.0148056E1]" — strip brackets
        _bs_str = str(_bs_str).strip('[]')
        base_score = float(_bs_str)
    except Exception:
        bs = model.get_params().get('base_score', None)
        base_score = float(bs) if bs is not None else 0.5

    lines = []
    lines.append(f"// Auto-generated XGBoost model for ESP32")
    lines.append(f"// Model: {name}")
    lines.append(f"#ifndef MODEL_{name.upper()}_H")
    lines.append(f"#define MODEL_{name.upper()}_H")
    lines.append(f"")
    lines.append(f"#define {name.upper()}_N_BASE_FEATURES {n_base}")
    lines.append(f"#define {name.upper()}_N_TREES {n_trees}")
    lines.append(f"#define {name.upper()}_BASE_SCORE {base_score:.6f}f")
    lines.append(f"")

    # Scaler
    lines.append(f"static const float {name}_scaler_mean[{n_base}] = {{")
    lines.append(f"  {', '.join(f'{v:.8f}f' for v in scaler_mean)}")
    lines.append(f"}};")
    lines.append(f"static const float {name}_scaler_scale[{n_base}] = {{")
    lines.append(f"  {', '.join(f'{v:.8f}f' for v in scaler_scale)}")
    lines.append(f"}};")
    lines.append(f"")

    # Emit each tree as a function
    for t_idx, nodes in enumerate(all_nodes):
        lines.append(f"static inline float {name}_tree{t_idx}(const float *f) {{")
        # iterative tree walk
        lines.append(f"  int node = 0;")
        lines.append(f"  while (1) {{")
        lines.append(f"    switch (node) {{")
        for n_idx, node in enumerate(nodes):
            feat, thresh_or_leaf, left, right = node
            if feat == -1:
                # leaf
                lines.append(f"      case {n_idx}: return {thresh_or_leaf:.8f}f;")
            else:
                lines.append(f"      case {n_idx}: node = (f[{feat}] < {thresh_or_leaf:.8f}f) ? {left} : {right}; break;")
        lines.append(f"      default: return 0.0f;")
        lines.append(f"    }}")
        lines.append(f"  }}")
        lines.append(f"}}")
        lines.append(f"")

    # Main predict function
    lines.append(f"static inline float {name}_predict(const float raw_features[{n_base}]) {{")
    lines.append(f"  float scaled[{n_base}];")
    lines.append(f"  for (int i = 0; i < {n_base}; i++) {{")
    lines.append(f"    scaled[i] = (raw_features[i] - {name}_scaler_mean[i]) / {name}_scaler_scale[i];")
    lines.append(f"  }}")
    lines.append(f"  float sum = {name.upper()}_BASE_SCORE;")
    for t_idx in range(n_trees):
        lines.append(f"  sum += {name}_tree{t_idx}(scaled);")
    lines.append(f"  return sum;")
    lines.append(f"}}")
    lines.append(f"")
    lines.append(f"#endif // MODEL_{name.upper()}_H")

    filepath = HEADER_DIR / filename
    filepath.write_text("\n".join(lines))
    print(f"  -> Exported {filepath} (manual tree export, {n_trees} trees)")


